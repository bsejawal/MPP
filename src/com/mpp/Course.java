package com.mpp;

public class Course {
	String number;
	String titla;
	int unit;
	
	
	
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getTitla() {
		return titla;
	}
	public void setTitla(String titla) {
		this.titla = titla;
	}
	public int getUnit() {
		return unit;
	}
	public void setUnit(int unit) {
		this.unit = unit;
	}
	
	
	

}
